function mostrarPropiedadesArray() {
    // Crear un arreglo de ejemplo
    var arreglo = [1, 43, 889, 7, 23, 56, 78, 4];
  
    // Ejemplo de uso 
    console.log("Ejemplo de uso de algunos métodos del objeto Array:");
    console.log("Longitud del arreglo: " + arreglo.length);
    console.log("Elemento en la posición 2: " + arreglo[2]);
    console.log("Índice del elemento 4: " + arreglo.indexOf(4));
    console.log("Arreglo convertido a cadena: " + arreglo.toString());
  }
  
  // Llamar a la función
  mostrarPropiedadesArray();
  